import type { MealRecipe, MealSuggestion } from '@/types';

type Ingredient = MealRecipe['ingredients'][number];

const ingredient = (
  name: string,
  quantity: number,
  unit: string,
  calories: number,
  protein: number,
  carbs: number,
  fats: number,
  source: string,
): Ingredient => ({
  name,
  quantity,
  unit,
  calories,
  protein,
  carbs,
  fats,
  source,
});

const createMeal = ({
  id,
  title,
  mealType,
  dietTypes,
  tags,
  ingredients,
  instructions,
  image,
}: {
  id: string;
  title: string;
  mealType: MealRecipe['mealType'];
  dietTypes: MealRecipe['dietTypes'];
  tags: string[];
  ingredients: Ingredient[];
  instructions: string[];
  image: string;
}): MealRecipe => {
  const totals = ingredients.reduce(
    (acc, item) => {
      acc.calories += item.calories;
      acc.protein += item.protein;
      acc.carbs += item.carbs;
      acc.fats += item.fats;
      return acc;
    },
    { calories: 0, protein: 0, carbs: 0, fats: 0 },
  );

  return {
    id,
    title,
    mealType,
    dietTypes,
    tags,
    ingredients,
    instructions,
    image,
    calories: Math.round(totals.calories),
    protein: Math.round(totals.protein),
    carbs: Math.round(totals.carbs),
    fats: Math.round(totals.fats),
  };
};

